public class Koordinat {
	protected int x;
	protected int y;

    // Konstruktor
    public Koordinat(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Metode getter dan setter untuk x
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    // Metode getter dan setter untuk y
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}