package COBAA;

//Kelas Mahasiswa
class Mahasiswa {
 // Atribut
 public String nama;
 public String jurusan;
 public int nrp;

 // Konstruktor
 public Mahasiswa() {
     nama = "";
     jurusan = "";
     nrp = 0;
 }
 public void setNama(String namasiswa) {
     nama = namasiswa;
 }
 public void setJurusan(String jurusansiswa) {
     jurusan = jurusansiswa;
 }
 public void setNrp(int nrpsiswa) {
     nrp = nrpsiswa;
 }

 public String setNama() {
     return nama;
 }
 public String setJurusan() {
     return jurusan;
 }
 public int setNrp() {
     return nrp;
 }
 public static void main(String[] args) {
     // Buat objek
     Mahasiswa mahasiswa1 = new Mahasiswa();
     Mahasiswa mahasiswa2 = new Mahasiswa();

     // Menggunakan set buat mengatur informasi mahasiswa
     mahasiswa1.setNama("Cita");
     mahasiswa1.setJurusan("Telkom");
     mahasiswa1.setNrp(7206);

     // Menggunakan set buat mengatur informasi mahasiswa
     mahasiswa2.setNama("Raka");
     mahasiswa2.setJurusan("IT");
     mahasiswa2.setNrp(7306);

     // Menggunakan metode info() untuk menampilkan informasi mahasiswa
     System.out.println("Informasi Mahasiswa 1:");
     mahasiswa1.info();

     System.out.println("Informasi Mahasiswa 2:");
     mahasiswa2.info();
 }
 //  buat menampilkan informasi mahasiswa
 public void info() {
     System.out.println("Nama	: " + setNama());
     System.out.println("Jurusan	: " + setJurusan());
     System.out.println("NPM	: " + setNrp());
     System.out.println();
}
}
